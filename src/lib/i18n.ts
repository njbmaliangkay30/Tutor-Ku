export function t(key: string, lang: Language): string {
  const keys = key.split('.');
  let current: any = translations[lang] || translations['id'];
  for (const k of keys) {
    if (current && current[k] !== undefined) {
      current = current[k];
    } else {
      return key; // fallback
    }
  }
  return typeof current === 'string' ? current : key;
}
